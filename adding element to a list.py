a = ["valid", "ball", "nice", "man", 'quick']
print(a)
a.append(123)  # append function adds the new element at the end of the list
print(a)
a.insert(0, 1234)  # insert function adds the new element at the assigned place. Here 0 is the position and 1234 is the
# new element.
print(a)
a.extend(["one", "two", "alpha"])  # to add extra elements to the list.
print(a)
a = a + ['alone', 'dark', 'night', 'tiger']  # a simple way to add elements to the list.
print(a)
