a = "Mohammad"
print(len(a))  # this command line will count the number of letters in variable 'a'.
print(a.count('m'))  # this command line will count the letter "m" in variable 'a'.
line = "she sells sea shells on the sea shore"
print(line.count('s'))  # this will count the number of s in the line.
print(line.count('s', 5))  # this will count the number of s in the line. It will start counting after the 5th letter.
