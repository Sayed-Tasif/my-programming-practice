word = "Bangladesh is our 'motherland'. I love Bangladesh."  # this is a way to use quotation mark in double quotation
print(word)
word3 = "Bangladesh is our \"motherland\". I love Bangladesh."  # this is one of the most efficient way to use double
                                                                # quotation
                                                                # inside another double quotation mark
print(word3)
print("\\")  # This is how you can print 'a' back slash.
print("I love milk\\chocolate.")  # if you use double "\" back slash, you will get one back slash as output.
